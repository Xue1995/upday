package com.xue.Common.Entity;

public class Student {
	private String xid;  //学员ID
	private String xname; //姓名（账号）
	private String xpwd;  //密码
	private String xsex;  //性别
	private String xage;  //年龄
	private String xmoney;  //学费
	private String xstatus; //科目
	private String fromadd; //地址
	public String getXid() {
		return xid;
	}
	public void setXid(String xid) {
		this.xid = xid;
	}
	public String getXname() {
		return xname;
	}
	public void setXname(String xname) {
		this.xname = xname;
	}
	public String getXpwd() {
		return xpwd;
	}
	public void setXpwd(String xpwd) {
		this.xpwd = xpwd;
	}
	public String getXsex() {
		return xsex;
	}
	public void setXsex(String xsex) {
		this.xsex = xsex;
	}
	public String getXage() {
		return xage;
	}
	public void setXage(String xage) {
		this.xage = xage;
	}
	public String getXmoney() {
		return xmoney;
	}
	public void setXmoney(String xmoney) {
		this.xmoney = xmoney;
	}
	public String getXstatus() {
		return xstatus;
	}
	public void setXstatus(String xstatus) {
		this.xstatus = xstatus;
	}
	public String getFromadd() {
		return fromadd;
	}
	public void setFromadd(String fromadd) {
		this.fromadd = fromadd;
	}


}
